public class EvenOdd {
    public static void main(String[] args) {
        System.out.println("Please Enter value of a : ");
        int a = 20;
        if (a % 2 == 0) {
            System.out.println(a + "   EVEN");
        }
        else {
            System.out.println(a + "   Odd");
        }
    }
}
